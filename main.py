"""
จงเขียนโปรแกรมสร้างโลกของเกม RPG โดยใช้ list เก็บรายชื่อมอนสเตอร์และสมบัติต่างๆ 
ในเกม โดยใช้ for loop วนซ้ำเพื่อแสดงรายชื่อมอนสเตอร์และสมบัติต่างๆ ออกมา
"""

# รายชื่อมอนสเตอร์สร้างมา 5 ตัวอย่างเป็น str
monsters = ["Magic","Tiger","God","King","Queen"]

# รายการสมบัติสร้างมา 5 ชิ้นอย่างเป็น str
treasures = ["Attack","Speed","Defend","Pass","Controll"]

# แสดงรายชื่อมอนสเตอร์
for i in monsters:
    print(i)

# แสดงรายการสมบัติ
for i in treasures:
    print(i)